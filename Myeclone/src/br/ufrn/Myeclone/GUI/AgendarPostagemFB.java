    private Time getHora(){
        
        Time t = null;
        
        if(!jthora.getText().equals("") && !jthora.getText().equals(":")){
            String time[] = jthora.getText().trim().split(":");
            
            int hora = Integer.parseInt(time[0]);
            int minutos = Integer.parseInt(time[1]);
            
            t = new Time(hora, minutos, 0);
        }
        
        return t;
    }

    
    private void jBnovoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBnovoActionPerformed
        post = new Postagem();
        post.setToken(token);
        post.setAtividade("postagemfb");
        post.setDescricao(jTextField1.getText());
        post.setData(jCdata.getDate());
        post.setHorario(getHora());
        post.setCumprido(false);
        post.setRecorrente(false);
        post.setTempoEstimado(0);
        postagens.add(post);
        adicionarPost(post);
        try {
            postService.create(post);
        } catch (ServiceException ex) {
            
        }
    }//GEN-LAST:event_jBnovoActionPerformed

    private void jBpesquisar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBpesquisar2ActionPerformed
        Postagem post = postagens.get(jTableAtividades.getSelectedRow());
        postagens.remove(jTableAtividades.getSelectedRow());
        tmPosts.removeRow(jTableAtividades.getSelectedRow());
        
        
        try {
            postService.destroy(post);
        } catch (ServiceException ex) {
            Logger.getLogger(AgendarPostagemFB.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_jBpesquisar2ActionPerformed

