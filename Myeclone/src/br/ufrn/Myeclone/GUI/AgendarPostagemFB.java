    private void exibirPosts(List<Postagem> postagens){
        
        while(tmPosts.getRowCount() > 0){
            tmPosts.removeRow(0);
        }
        
        String [] campos = new String[]{null, null, null, null};
        
        for(int i = 0; i < postagens.size(); i++){
            tmPosts.addRow(campos);
            
            tmPosts.setValueAt(postagens.get(i).getDescricao(), i, 0);
            tmPosts.setValueAt(postagens.get(i).getData(), i, 1);
            tmPosts.setValueAt(postagens.get(i).getHorario(), i, 2);
            tmPosts.setValueAt(postagens.get(i).isCumprido(), i, 3);
        }  
    }

    private void adicionarPost(Postagem postagem){
        String [] campos = new String[]{null, null, null, null};
        tmPosts.addRow(campos);
        int rowCount = tmPosts.getRowCount();
        tmPosts.setValueAt(postagem.getDescricao(), rowCount-1, 0);
        tmPosts.setValueAt(postagem.getData(), rowCount-1, 1);
        tmPosts.setValueAt(postagem.getHorario(), rowCount-1, 2);
        tmPosts.setValueAt(postagem.isCumprido(), rowCount-1, 3);
    }
    
